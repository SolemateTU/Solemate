        @Override
        public void onPictureTaken(CameraView cameraView, final byte[] data) {
            final File[] fil = new File[100];
            Log.d(TAG, "onPictureTaken " + data.length);
            Toast.makeText(cameraView.getContext(), R.string.picture_taken, Toast.LENGTH_SHORT)
                    .show();

            getBackgroundHandler().post(new Runnable() {
                @Override
                public void run() {
                    int min = 0;
                    int max = 999999999;

                    Random r = new Random();
                    int rand = r.nextInt(max - min + 1) + min;
                    File file = new File(getExternalFilesDir(Environment.DIRECTORY_PICTURES), Integer.toString(rand)+".jpg");

                    Intent mediaScanIntent = new Intent(Intent.ACTION_MEDIA_SCANNER_SCAN_FILE);

                    fil[counter] =file;
                    counter=counter++;
                    System.out.println("counter before: "+counter);
                    editor.putInt("count", counter);
                    editor.commit();
                    System.out.println("counter: "+counter);

                    f=file;
                    OutputStream os = null;
                    try {
                        os = new FileOutputStream(file);
                        os.write(data);
                        os.close();
                    } catch (IOException e) {
                        Log.w(TAG, "Cannot write to " + file, e);
                    } finally {
                        if (os != null) {
                            try {
                                os.close();
                            } catch (IOException e) {
                                // Ignore
                            }
                        }
                    }

                    Uri contentUri = Uri.fromFile(fil[counter]);
                    mediaScanIntent.setData(contentUri);
                    MainActivity.this.sendBroadcast(mediaScanIntent);

                    Intent intent = new Intent(MainActivity.this, Details.class);
                    intent.putExtra("picture", f);
                    intent.putExtra("boolean", false);
                    startActivity(intent);
                }
            });
        }

